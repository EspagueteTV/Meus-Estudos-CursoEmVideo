n = input('Digite algo: ')
print('O \033[1;31m tipo primitivo\033[m desse valor é: ', type(n))
print('Só tem \033[1;32m espaços \033[m:', n.isspace())
print('É um \033[1;31m número\033[m: ', n.isnumeric())
print('É \033[1;32m alfabético\033[m: ', n.isalpha())
print('É \033[1;31m alfanumérico\033[m: ', n.isalnum())
print('Está em \033[1;32m maiúsculas\033[m: ', n.isupper())
print('Está em \033[1;31m minúsculas\033[m: ', n.islower())
print('Está \033[1;32m capitalizada\033[m: ', n.istitle())
